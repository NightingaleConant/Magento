$installer = $this;
$installer->startSetup();

$this->_setupCache = array();

$attributeApplyTo = explode(',', $installer->getAttribute('catalog_product', 'image', 'apply_to'));
foreach ($attributeApplyTo as $key => $value)
    if ($value == 'subscription_configurable') unset($attributeApplyTo[$key]);

$installer->updateAttribute('catalog_product', 'image', 'apply_to', implode(',', $attributeApplyTo));

$installer->endSetup();